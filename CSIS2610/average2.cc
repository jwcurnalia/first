#include <iostream>

using namespace std;

int main(void) {
  int grade,sum=0,count=0;

  cout << "Enter a grade (-1 to quit): ";
  cin >> grade;

  while (grade >= 0) {
    sum += grade;
    count++;

    cout << "Enter a grade (-1 to quit): ";
    cin >> grade;
  }

  cout << "Average: " << sum / count << endl;

  return 0;
}
